package com.zefun.web.entity.bbs;

public class BbsSetting {
    private Integer storeId;

    private Integer singleSignReward;

    private Integer serialDaysNumber;

    private Integer serialDaysReward;

    private Integer publishReward;

    private Integer commentReward;

    private Integer commendReward;

    private Integer shareReward;

    private Integer topReward;

    public Integer getStoreId() {
        return storeId;
    }

    public void setStoreId(Integer storeId) {
        this.storeId = storeId;
    }

    public Integer getSingleSignReward() {
        return singleSignReward;
    }

    public void setSingleSignReward(Integer singleSignReward) {
        this.singleSignReward = singleSignReward;
    }

    public Integer getSerialDaysNumber() {
        return serialDaysNumber;
    }

    public void setSerialDaysNumber(Integer serialDaysNumber) {
        this.serialDaysNumber = serialDaysNumber;
    }

    public Integer getSerialDaysReward() {
        return serialDaysReward;
    }

    public void setSerialDaysReward(Integer serialDaysReward) {
        this.serialDaysReward = serialDaysReward;
    }

    public Integer getPublishReward() {
        return publishReward;
    }

    public void setPublishReward(Integer publishReward) {
        this.publishReward = publishReward;
    }

    public Integer getCommentReward() {
        return commentReward;
    }

    public void setCommentReward(Integer commentReward) {
        this.commentReward = commentReward;
    }

    public Integer getCommendReward() {
        return commendReward;
    }

    public void setCommendReward(Integer commendReward) {
        this.commendReward = commendReward;
    }

    public Integer getShareReward() {
        return shareReward;
    }

    public void setShareReward(Integer shareReward) {
        this.shareReward = shareReward;
    }

    public Integer getTopReward() {
        return topReward;
    }

    public void setTopReward(Integer topReward) {
        this.topReward = topReward;
    }
}